def val_my(dataloader, model, compute_loss, save_dir="./val_runs/train/exp_l", nc=80):
    """
    计算yolo的mAP
    :param dataloader: 验证集的dataloader
    :param model: 模型
    :param compute_loss: 损失函数
    :param save_dir: 结果保存路径
    :param nc: 类别数
    :return: 无
    """
    device = next(model.parameters()).device
    names = model.names if hasattr(model, 'names') else model.module.names  # get class names
    if isinstance(names, (list, tuple)):  # old format
        names = dict(enumerate(names))
    seen = 0    # 计数
    stats = []  # 存储用于计算map的数据
    iouv = torch.linspace(0.5, 0.95, 10, device=device)  # iou vector for mAP@0.5:0.95
    niou = iouv.numel()
    s = ('%22s' + '%11s' * 6) % ('Class', 'Images', 'Instances', 'P', 'R', 'mAP50', 'mAP50-95')
    pbar = tqdm(dataloader, desc=s, bar_format=TQDM_BAR_FORMAT)
    with torch.no_grad():
        model.eval()
        for batch_i, (im, targets, paths, shapes) in enumerate(pbar):
            # 数据预处理
            im = im.to(device).float()
            im /= 255
            nb, _, height, width = im.shape
            targets = targets.to(device)

            # 前向传播
            preds, train_out = model(im)
            im = im.cpu()

            # 损失计算
            loss = torch.zeros(3, device=device)
            loss += compute_loss(train_out, targets)[1]  # box, obj, cls

            # 非极大值抑制
            targets[:, 2:] *= torch.tensor((width, height, width, height), device=device)  # to pixels
            lb = [targets[targets[:, 0] == i, 1:] for i in range(nb)] if False else []  # for autolabelling
            preds = non_max_suppression(preds,
                                        0.001,
                                        0.6,
                                        labels=lb,
                                        multi_label=True,
                                        agnostic=False,
                                        max_det=300)

            # 进一步处理非极大值抑制之后的结果
            for si, pred in enumerate(preds):
                labels = targets[targets[:, 0] == si, 1:]
                nl, npr = labels.shape[0], pred.shape[0]  # number of labels, predictions
                path, shape = Path(paths[si]), shapes[si][0]
